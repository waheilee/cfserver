    private function genSign($data, $Md5key)
    {
        ksort($data);
        $md5str = '';
        foreach ($data as $key => $val) {
            if (!empty(trim($val))) {
                $md5str = $md5str . $key . '=' . $val . '&';
            }
        }
        return strtolower(md5($md5str . 'key=' . $Md5key));
    }


    public function makeSignature(&$payload,$Md5key)
    {
        ksort($payload);
        $un_hashed_str = "";
        foreach ($payload as $item) {
            $un_hashed_str = $un_hashed_str . $item;
        }
        return base64_encode(hash_hmac("sha1", $un_hashed_str, $Md5key, $raw_output = TRUE));
    }


